from CUST_ORDER;

--1
SELECT NAME AS CustumerSinOrdenes
FROM CUSTOMER C
LEFT JOIN CUST_ORDER O ON C.CUST_NBR = O.CUST_NBR
WHERE C.CUST_NBR IS NULL
ORDER BY CustumerSinOrdenes DESC;

--2 

SELECT ESPECIALIDAD, COUNT(*) AS Cantidad_Doctores
FROM DOCTORES
GROUP BY ESPECIALIDAD;


--3

SELECT DEPT_ID, MAX(SALARY) AS Salario_Maximo
FROM EMPLOYEE
GROUP BY DEPT_ID;   


--4

 SELECT fecha from CITAS
