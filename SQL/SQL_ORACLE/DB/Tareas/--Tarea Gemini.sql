--5

SELECT D.NAME, AVG(E.SALARY)
FROM DEPARTMENT D 
JOIN EMPLOYEE E ON D.DEPT_ID = E.DEPT_ID
GROUP BY D.NAME
HAVING COUNT(E.EMP_ID) > 3; -- Filtra grupos que tengan más de 3 empleados


--6 

SELECT C.NAME, SUM(O.TOT_SALES) AS "MontoTotalGastado"
FROM CUSTOMER C 
JOIN ORDERS O ON C.CUST_NBR = O.CUST_NBR
GROUP BY C.NAME;


--7 

SELECT d.NOMBREDOCTOR,COUNT(DISTINCT p.NOMBREPACIENTE) as "pacientesUnicos"
from DOCTORES d 
JOIN CITAS c ON d.IDDOCTOR = c.DOCTOR
JOIN PACIENTES p on c.PACIENTE = p.IDPACIENTE
GROUP BY d.NOMBREDOCTOR
ORDER by "pacientesUnicos" DESC;


